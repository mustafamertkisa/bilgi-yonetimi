        <h1>Kan Bağışı Hayati Bir Önem Taşır!</h1><hr><br>
        <p>Kan, en temel yaşam kaynağıdır. Bir canlıda, kan akışının durması demek, hayatının sona ermesi anlamına gelmektedir. Yetişkin bir insanda ortalama 5-6 litre kan bulunur. Kan, akciğerden aldığı oksijeni dokulara ve dokularda üretilen karbondioksidi akciğere taşır. Gerekli besin ve hormonları, dokuların süzgecinden geçirir. İletirken de, artık maddeleri atmak için akciğer, karaciğer ve böbrek gibi organlara taşır. Ayrıca vücut ısı dengesini sağlar, asit-baz dengesini korur. Kanserojen maddelerden, yabancı maddelerden, mikroplardan ve virüslerden korur. Pıhtılaşma yaparak kan kayıplarını önler. İnsanların vücudunda gerekenden az bulunduğu durumlarda, kan alışverişinin yapılmasına neden olmaktadır. Kanın, insan hayatındaki önemi hayati seviyededir.</p>        
        <h3>Kan Bağışı Neden Önemlidir?</h3>
        <p>Yılda bir veya iki kez yapılacak olan kan bağışı birçok insana yaşam umudu olabilir. Her gün çeşitli kazalar, yaralanmalar ve operasyonlar gerçekleşmektedir. Bu noktada, kan ihtiyacı hayati önem taşır. Kanamalı hastalığı olan bir hastanın, ameliyatta 6-8 ünite kana ihtiyacı olabilmektedir. Bu hastanın, kurtulabilmesi için kan gereksinimi olduğu unutulmamalıdır. Kan bağışının olmadığı bir toplumda, her gün binlerce kişi hayatını kaybedebilir. Bu sebep ile, düzenli kan bağışında bulunmak insanların hayatını kurtarır. Kan bağışı, bir sosyal sorumluluk olup, bir gün herkesin kana ihtiyaç duyacağı da bir gerçektir. Kan bağışının ücretsiz gönüllülük esasında yapıldığının da unutulmaması ve bu bilinç ile yapılması gerekir. Kan bağışı yapan kişi ile aynı soyadı taşıyan yakınlarına kan bağışında öncelik tanınarak kan sigortası sağlanabilmektedir.</p>
